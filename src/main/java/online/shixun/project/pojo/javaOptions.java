package online.shixun.project.pojo;

public class javaOptions {

	private int id;
	private String topic;
	private int javapractise_id;
	private int isture;
	private javapractise javapractis;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTopic() {
		return topic;
	}
	public void setTopic(String topic) {
		this.topic = topic;
	}
	public int getJavapractise_id() {
		return javapractise_id;
	}
	public void setJavapractise_id(int javapractise_id) {
		this.javapractise_id = javapractise_id;
	}
	public int getIsture() {
		return isture;
	}
	public void setIsture(int isture) {
		this.isture = isture;
	}
	public javapractise getJavapractis() {
		return javapractis;
	}
	public void setJavapractis(javapractise javapractis) {
		this.javapractis = javapractis;
	}
	
}
